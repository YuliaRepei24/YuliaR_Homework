const regularStr = /^(?!.*[Aa]).{6,}$/; // Наш регулярний вираз (перевірка на "Аа" та що слово 6+ літер);
const str = "Apples"; // Приклад рядка;
const result = regularStr.test(str); //Ця стрічка коду виконує перевірку рядка str на відповідність регулярному виразу 
console.log(result); // Поверне true або false в залежності від того, чи відповідає рядок регулярному виразу (якщо відповідає то буде true, якщо ні то false);
